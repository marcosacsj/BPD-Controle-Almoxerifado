    private void CarregarFornecedor() throws SQLException {
        String sql = "select nome_fantasia from fornecedor order by nome_fantasia;";
       
       //RegiÃ£o de exclusÃ£o dos dados anteriores
       cmbFornecedorPapelaria.removeAllItems();

       
       con.conecta(NomeBanco);
       con.executaSelect(sql);
       rs=con.resultset;
       while(rs.next()){
           cmbFornecedorPapelaria.addItem(rs.getString("nome_fantasia"));

       }
       con.desconecta();
    }

    private void CarregarTransportadora() throws SQLException {
        String sql = "select nome_fantasia from transportadora order by nome_fantasia;";
       
       //RegiÃ£o de exclusÃ£o dos dados anteriores
       cmbTransportadora.removeAllItems();
       cmbTransportadora.addItem("Sem transportadora");
       
       con.conecta(NomeBanco);
       con.executaSelect(sql);
       rs=con.resultset;
       while(rs.next()){
           cmbTransportadora.addItem(rs.getString("nome_fantasia"));

       }
       con.desconecta();
    }
