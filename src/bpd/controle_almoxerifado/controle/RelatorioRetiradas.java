    private void GerarRelatorio() throws JRException {
        String Datainicial = formata.format(dtInicial.getDate());
        String sql="Select * from retiradas where data_retirada>='"+Datainicial+"' "
                + "group by data_retirada,descricao,quantidade,funcionario,insumo,idretiradas;";
        relatorios.Gerar("Retiradas", sql, NomeBanco);
    }
