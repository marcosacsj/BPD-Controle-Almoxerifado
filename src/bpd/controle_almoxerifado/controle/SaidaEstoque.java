    private void AtualizaPapelaria() throws SQLException {
        boolean autorizado=ModuloAutoriza();
        if(autorizado){
            int quantidade=ModuloCalculo();
            String sql="update papelaria set "
                    + "quantidade='"+quantidade+"' "
                    + "where idpapelaria='"+txtID.getText().trim()+"';";
            con.conecta(NomeBanco);
            con.executa(sql);
            con.desconecta();
            GravarRelatorio("Papelaria");
        }else{
            InOut.MsgDeErro(null, "Operação não autorizada!\nQuantidade insuficiente em estoque.");
        }
    }

