    private void PesquisaPapelaria() throws SQLException {
        String sql="select idpapelaria,insumo from papelaria where insumo like'%"+txtPesquisa.getText().trim()+"%' order by insumo;";
        DefaultTableModel modelo1 = (DefaultTableModel) tbPesquisa.getModel();
        con.conecta(NomeBanco);
        con.executaSelect(sql);
        rs = con.resultset;
        modelo1.setRowCount(0);
        while(rs.next()){
            
            modelo1.addRow(new Object[]{
                          rs.getString("idpapelaria"),
                          rs.getString("insumo")
            });
        }
        con.desconecta();
    }
